#include <bits/stdc++.h>
using namespace std;
int main(){
    //post increment
    int a=1;
    cout<<"before a= "<<a<<endl;
    int b=a++;
    cout<<"after a = "<<a<<endl<<"b= "<<b<<endl;
    //pre increment
    int c=1;
    cout<<"before c= "<<c<<endl;
    int d=++c;
    cout<<"after c = "<<c<<endl<<"d= "<<d<<endl;

}