
#include <bits/stdc++.h>

using namespace std;
int main()
{
    int a = 10, b = 100;
    bool result;
    result = (a==b);
    cout<<"a==b "<<result<<endl;
    result = (a!=b);
    cout<<"a!=b "<<result<<endl;
    result = (a<b);
    cout<<"a<b "<<result<<endl;
    result = (a<=b);
    cout<<"a<=b "<<result<<endl;
    result = (a>=b);
    cout<<"a>=b "<<result<<endl;
    return 0;
    //relational operators
}