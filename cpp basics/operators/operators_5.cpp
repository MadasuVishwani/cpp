
//assignment operators
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a=10;
    a+=1;
    cout<<"a+= "<<a<<endl;
    a=10;
    a-=1;
    cout<<"a-= "<<a<<endl;
    a*= 2;
    cout<<"a*= "<<a<<endl;
    a/=2;
    cout<<"a/= "<<a<<endl;
    a%=2;
    cout<<"a%= "<<a<<endl;
    
    
    



    return 0;

}