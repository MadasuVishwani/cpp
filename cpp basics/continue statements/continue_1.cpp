//continue  is for continuing the loop. it skips at the specified condition and continues the remaining flow of the code
#include <bits/stdc++.h>
using namespace std;
int main()
{
    for (int i=1;i<=12;i++)
    {

        if(i==9)
        {
            continue;
        }
        cout<<i<<"\n";
    
    }
}
